
public class PruebaVariables {

	public static void main(String[] args) {
		boolean estaEncendido = true;
		if(estaEncendido) {
			System.out.println("La tv esta encendida");
		} else {
			System.out.println("La tv esta apagada");
		}
		if(!estaEncendido) {
			System.out.println("La tv esta encendida");
		} else {
			System.out.println("La tv esta apagada");
		}
	}

}
